class BitwiseAnd
{
	public static void main(String args[])
	{
		
		int a = 10 & 7;
		System.out.println(a);

		int b = 12 & 9;
		System.out.println(b);

		int c = 10 & 5;
		System.out.println(c);

		int d = 15 & 5;
		System.out.println(d);

		int e = 15 & 16;
		System.out.println(e);

	}
}